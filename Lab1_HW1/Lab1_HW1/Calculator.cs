namespace Lab1_HW1
{
    class Calculator
    {
        public double Sub(double a1, double a2)
        {
            return a1 + a2;
        }

        public double Izv(double a1, double a2)
        {
            return a1 - a2;
        }

        public double Umn(double a1, double a2)
        {
            return a1 * a2;
        }
        public double Del(double a1, double a2)
        {
            return a1 / a2;
        }
    }
